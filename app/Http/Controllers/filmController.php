        $data = $request->all();
        $film = new Film;
        $film->title = $data['title'];
        $film->director = $data['director'];
        $film->cast = $data['cast'];
        $film->price = $data['price'];
        $film->year = $data['year'];
        $film->production_house = $data['production_house'];
        $save = $film->save();
        if ($save) {
            return redirect()->route('films.index');
        }
    }

